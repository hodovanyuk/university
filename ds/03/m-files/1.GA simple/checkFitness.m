function RES=checkFitness(CH)
% Визначення фітнесу хромосоми CH для матриці інцидентностей,
%       що знаходиться у глобальній змінній IM
% CH  - хромосома
% RES - значення функції фітнесу

global IM;
% визначаємо кількість міст
[n,n]=size(IM);
% визначаємо найбільший можливий шлях - верхня межа значення фітнесу
MAX=max(max(IM));
MAX=MAX*n;
% визначаємо довжину шляху для даної хромосоми
RES=0;
for i=1:n-1
    RES=RES+IM(CH(i),CH(i+1));
end
RES=RES+IM(CH(n),CH(1));
RES=MAX-RES;