function [ X,Y ] = initDE( N,D,L,U,f )
% Побудова початкової популяції для алгоритму ДЕ
% Вхід:
%    N - кількість векторів у популяції
%    D - розмірність простору
%    L - нижня межа значень для кожної координати
%    U - верхня межа значень для кожної координати
% Вихід:
%    X - початкова популяція векторів

X = zeros( N,D );
Y = zeros( N,1 );
for i = 1:N
    rnd = rand( 1,D );
    X( i,: ) = rnd .* ( U - L ) + L;
    Y( i ) = f( X( i,: ) );
end
